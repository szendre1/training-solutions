package algorithmssum;

public class Salesperson {
    private String nane;
    private long amount;

    public Salesperson(String nane, long anount) {
        this.nane = nane;
        this.amount = anount;
    }

    public String getNane() {
        return nane;
    }

    public long getAmount() {
        return amount;
    }
}
